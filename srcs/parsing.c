int		*ft_arraydup(int *input, size_t size)
{
	size_t	i;
	int		*result_array;

	i = 0;
	if (!(result_array = malloc(sizeof(result_array) * size)))
		return (NULL);
	while (i < size)
	{
		result_array[i] = input[i];
		i++;
	}
	return (result_array);
}

